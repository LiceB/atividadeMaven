package br.fiap.app.exemplo.models;

public class Categoria {
	private int id;
	private String descricao;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String decricao) {
		this.descricao = decricao;
	}

}
